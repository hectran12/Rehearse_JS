// Toán tử gán
var a = 5;

// += 
a += 2;
console.log(a);
// -=
a -= 2;
console.log(a);
// *=
a *= 5;
console.log(a);
// /= 
a /= 5;
console.log(a);
// **=
a **= 5;
console.log(a);

// Hậu tố
var numberA = 5;
numberA++;
console.log(numberA);

numberA--;
console.log(numberA);

// Tiền tố
var numberB = 6;
console.log(++numberB);
console.log(numberB);

console.log(--numberB);
console.log(numberB);