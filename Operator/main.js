// Khai báo biến 
var numA = 10;
var numB = 20;

// tính tổng
var tong = numA + numB;
console.log("Tổng là: "+tong);

// tính chia
var chia = numB / numA;
console.log("Chia là: "+chia);

// tính trừ
var tru = numB - numA;
console.log("Trừ là: "+tru);

// tính lũy thừa
var luyThua = numB ** numA;
console.log("Lũy thừa: "+luyThua);

// tính nhân
var Nhan = numA * numB;
console.log("Nhân là: "+Nhan);

// Chia lấy số dư
var ChiaLaySoDu = numA % numB;
console.log("Chia lấy số dư là: "+ChiaLaySoDu);

// Tăng một giá trị
numA++;
console.log("Tăng một giá trị là: "+numA);

// Bớt đi một giá trị
numB--;
console.log("Bớt đi một giá trị là: "+numB);
